window.onload = function () {
  var subjectSel = document.getElementById("subject");
  var topicSel = document.getElementById("topic");
  var chapterSel = document.getElementById("chapter");
  var competitionSel = document.getElementById("competition");

  // Rellenar competiciones
    competitionObject.forEach(function(comp) {
        competitionSel.options[competitionSel.options.length] = new Option(comp, comp);
    });

  for (var x in subjectObject) {
    subjectSel.options[subjectSel.options.length] = new Option(x, x);
  }
  subjectSel.onchange = function() {
    chapterSel.length = 1;
    topicSel.length = 1;
    for (var y in subjectObject[this.value]) {
        topicSel.options[topicSel.options.length] = new Option(y, y);
    }

    // 🔹 Seleccionar competición por defecto según la categoría
    var selectedCategory = this.value;
    var competitionSel = document.getElementById("competition");
    if (categoryCompetitionMap[selectedCategory]) {
        competitionSel.value = categoryCompetitionMap[selectedCategory];
    } else {
        competitionSel.value = ""; // vuelve a "Competición" si no hay asignación
    }
}

  topicSel.onchange = function () {
    //empty Chapters dropdown
    chapterSel.length = 1;
    //display correct values
    var z = subjectObject[subjectSel.value][this.value];
    for (var i = 0; i < z.length; i++) {
      chapterSel.options[chapterSel.options.length] = new Option(z[i], z[i]);
    }
  }
  competitionSel.onchange = function() {
    var customInput = document.getElementById("customCompetition");
    if (this.value === "OTRA") {
        customInput.style.display = "block";
    } else {
        customInput.style.display = "none";
        customInput.value = ""; // limpiar por si se cambia de opinión
    }
}

};
